export const getCropLabel = (crop: typeof allIndianCrops[0], language: string) => {
  switch (language) {
    case 'mr': return crop.mr;
    case 'hi': return crop.hi;
    default: return crop.en;
  }
};

// Build a mapping from crop id to all its names for product matching
export const buildCropMapping = (): Record<string, string[]> => {
  const mapping: Record<string, string[]> = {};
  allIndianCrops.forEach(crop => {
    mapping[crop.id] = [crop.en, crop.mr, crop.hi, crop.id];
  });
  return mapping;
};
